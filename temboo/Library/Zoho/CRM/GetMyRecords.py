from temboo.core.choreography import Choreography
from temboo.core.choreography import InputSet
from temboo.core.choreography import ResultSet
from temboo.core.choreography import ChoreographyExecution


class GetMyRecords(Choreography):

    """
    Create a new instance of the GetMyRecords Choreography. A TembooSession object, containing a valid
    set of Temboo credentials, must be supplied.
    """
    def __init__(self, temboo_session):
        Choreography.__init__(self, temboo_session, '/Library/Zoho/CRM/GetMyRecords')


    def new_input_set(self):
        return GetMyRecordsInputSet()

    def _make_result_set(self, result, path):
        return GetMyRecordsResultSet(result, path)

    def _make_execution(self, session, exec_id, path):
        return GetMyRecordsChoreographyExecution(session, exec_id, path)

